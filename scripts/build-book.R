#!/usr/bin/env Rscript

# =============================================================================
# build-book.R
# -----------------------------------------------------------------------------
# Generates the Quarto book under book/ from the author-edited Markdown files
# in case-studies/.
#
# For each case-studies/NN-slug/case-file*.md it:
#   * reads the YAML front matter (number, title, detective, difficulty,
#     topics, fallacies, summary)
#   * splits the body into a CASE part (crime scene + exhibits + interrogation)
#     and a SOLUTION part (answers + what went wrong + background + sources)
#   * copies any referenced images into the book and rewrites the links
#   * writes book/cases/case-NN.qmd and book/solutions/solution-NN.qmd
#
# It also (re)generates:
#   * book/cases/overview.qmd        – sortable table of all cases
#   * book/solutions/index.qmd       – landing page for the solutions part
#   * book/references.qmd            – aggregated bibliography
#   * book/appendix-index.qmd        – tag index (topic / fallacy / difficulty)
#   * book/intro/detectives.qmd      – from case-studies/detectives.md
#   * book/intro/world.qmd           – from case-studies/lore.md
#   * book/_quarto.yml               – the book definition + chapter list
#
# Authors normally only ever touch the Markdown in case-studies/. To add a new
# case, drop a new NN-slug/ folder with a case-file*.md (front matter + the
# usual section headings) and re-run this script.
#
# Usage:  Rscript scripts/build-book.R
# =============================================================================

suppressPackageStartupMessages({
  library(yaml)
  library(stringr)
})

# ---- configuration (edit here to change book-level metadata) ----------------

BOOK <- list(
  title    = "Crimes Against Causality",
  subtitle = "An interactive mystery-solving book about flawed scientific evidence",
  authors  = c("Simon Munzert", "Dylan Thurgood"),
  site_url = "https://crimes-against-causality.github.io/",
  repo_url = "https://github.com/crimes-against-causality/crimes-against-causality.github.io",
  # difficulty code -> human label
  difficulty_labels = c("1" = "Easy", "2" = "Medium", "3" = "Hard")
)

# ---- paths ------------------------------------------------------------------

root        <- normalizePath(file.path(dirname(sub("--file=", "",
                 grep("--file=", commandArgs(FALSE), value = TRUE)[1])), ".."),
                 mustWork = FALSE)
if (is.na(root) || !nzchar(root)) root <- normalizePath(".")
cases_src   <- file.path(root, "case-studies")
book_dir    <- file.path(root, "book")
dir_cases   <- file.path(book_dir, "cases")
dir_solns   <- file.path(book_dir, "solutions")
dir_intro   <- file.path(book_dir, "intro")

for (d in c(book_dir, dir_cases, dir_solns, dir_intro,
            file.path(dir_cases, "assets"), file.path(dir_solns, "assets"))) {
  dir.create(d, showWarnings = FALSE, recursive = TRUE)
}

GENERATED_NOTE <- paste0(
  "<!-- DO NOT EDIT. Generated by scripts/build-book.R from the Markdown\n",
  "     in case-studies/. Edit the source case files and re-run the build. -->\n\n")

# ---- helpers ----------------------------------------------------------------

`%||%` <- function(a, b) if (is.null(a) || (length(a) == 1 && is.na(a))) b else a

# Write a character vector to disk as UTF-8, regardless of the session locale.
write_utf8 <- function(lines, path) {
  con <- file(path, open = "wb")
  on.exit(close(con))
  writeLines(enc2utf8(as.character(lines)), con, useBytes = TRUE)
}

# Strip the cosmetic inline HTML that some case files use in headings/text.
clean_inline_html <- function(x) {
  x <- str_replace_all(x, "<br\\s*/?>", " ")
  x <- str_replace_all(x, "</?span[^>]*>", "")
  str_squish(x)
}

# Read a case file and return list(meta = <yaml>, body = <character vector>).
read_case <- function(path) {
  lines <- readLines(path, warn = FALSE, encoding = "UTF-8")
  if (length(lines) == 0 || !str_detect(lines[1], "^---\\s*$")) {
    stop("Case file is missing YAML front matter: ", path)
  }
  close_idx <- which(str_detect(lines, "^---\\s*$"))[2]
  if (is.na(close_idx)) stop("Unterminated YAML front matter: ", path)
  meta <- yaml.load(paste(lines[2:(close_idx - 1)], collapse = "\n"))
  body <- lines[(close_idx + 1):length(lines)]
  # drop the leading H1 title line (we render the title from front matter)
  first_h1 <- which(str_detect(body, "^#\\s"))[1]
  if (!is.na(first_h1)) body <- body[-(1:first_h1)]
  # trim leading blank lines
  while (length(body) && !nzchar(str_trim(body[1]))) body <- body[-1]
  list(meta = meta, body = body)
}

# Index of heading lines whose (cleaned) text matches a regex, case-insensitive.
heading_matching <- function(body, pattern) {
  is_h    <- str_detect(body, "^#{1,6}\\s")
  cleaned <- clean_inline_html(str_replace(body, "^#{1,6}\\s*", ""))
  which(is_h & str_detect(cleaned, regex(pattern, ignore_case = TRUE)))
}

# Split a body into the case half and the solution half at the "Answers" heading.
split_case_solution <- function(body, slug) {
  ans <- heading_matching(body, "^answers?\\b")
  if (length(ans) == 0) {
    warning("No 'Answers' heading found in ", slug,
            " \u2014 treating whole file as the case.")
    return(list(case = body, solution = character(0)))
  }
  cut <- ans[1]
  list(case = body[seq_len(cut - 1)], solution = body[cut:length(body)])
}

# Copy referenced local images for one body part into <dest>/assets and rewrite
# the links. Returns the rewritten body.
relocate_images <- function(body, src_dir, dest_dir, prefix) {
  img_re <- "!\\[[^]]*\\]\\(([^)]+)\\)"
  for (i in seq_along(body)) {
    m <- str_match_all(body[i], img_re)[[1]]
    if (nrow(m) == 0) next
    for (j in seq_len(nrow(m))) {
      path <- m[j, 2]
      if (str_detect(path, "^https?://")) next
      src <- file.path(src_dir, path)
      if (!file.exists(src)) {
        warning("Missing image referenced in ", basename(src_dir), ": ", path)
        next
      }
      newname <- paste0(prefix, "-", basename(path))
      file.copy(src, file.path(dest_dir, "assets", newname), overwrite = TRUE)
      body[i] <- str_replace(body[i], stringr::fixed(path),
                             file.path("assets", newname))
    }
  }
  body
}

# Clean heading lines so the TOC / anchors are tidy (keeps body text as-is).
tidy_headings <- function(body) {
  is_h <- str_detect(body, "^#{1,6}\\s")
  body[is_h] <- vapply(body[is_h], function(h) {
    hashes <- str_extract(h, "^#{1,6}")
    paste(hashes, clean_inline_html(str_replace(h, "^#{1,6}\\s*", "")))
  }, character(1))
  body
}

difficulty_label <- function(d) {
  lbl <- BOOK$difficulty_labels[as.character(d)]
  if (is.na(lbl)) as.character(d) else unname(lbl)
}
difficulty_dots <- function(d) {
  d <- suppressWarnings(as.integer(d)); if (is.na(d)) return("")
  paste0(strrep("\u25cf", d), strrep("\u25cb", max(0, 3 - d)))
}

as_vec <- function(x) if (is.null(x)) character(0) else unlist(x, use.names = FALSE)

pad2 <- function(n) sprintf("%02d", as.integer(n))

# ---- discover cases ---------------------------------------------------------

case_dirs <- list.dirs(cases_src, recursive = FALSE, full.names = TRUE)
case_dirs <- case_dirs[str_detect(basename(case_dirs), "^[0-9]+-")]
case_dirs <- case_dirs[order(as.integer(str_extract(basename(case_dirs), "^[0-9]+")))]

cases <- list()
for (cd in case_dirs) {
  cf <- list.files(cd, pattern = "^case-file.*\\.md$", full.names = TRUE)
  if (length(cf) == 0) {
    warning("No case-file*.md in ", cd, " \u2014 skipping.")
    next
  }
  cf <- cf[1]
  parsed <- read_case(cf)
  meta   <- parsed$meta
  num    <- as.integer(meta$number %||% str_extract(basename(cd), "^[0-9]+"))
  n2     <- pad2(num)

  parts    <- split_case_solution(parsed$body, meta$slug %||% basename(cd))
  case_body <- tidy_headings(relocate_images(parts$case, cd, dir_cases, n2))
  soln_body <- tidy_headings(relocate_images(parts$solution, cd, dir_solns, n2))

  cases[[length(cases) + 1]] <- list(
    number = num, n2 = n2, dir = cd,
    title = meta$title %||% basename(cd),
    detective = meta$detective %||% "",
    difficulty = meta$difficulty %||% NA,
    topics = as_vec(meta$topics),
    fallacies = as_vec(meta$fallacies),
    summary = meta$summary %||% "",
    case_body = case_body, soln_body = soln_body
  )
}

if (length(cases) == 0) stop("No cases found under ", cases_src)
message("Found ", length(cases), " case(s).")

# ---- write per-case and per-solution chapters -------------------------------

badge_line <- function(cs) {
  paste0("**Detective:** ", cs$detective,
         "  \u00b7  **Difficulty:** ", difficulty_label(cs$difficulty),
         " ", difficulty_dots(cs$difficulty))
}
tag_line <- function(cs) {
  paste0("**Topics:** ", paste(cs$topics, collapse = ", "),
         "  \u00b7  **Fallacies:** ", paste(cs$fallacies, collapse = ", "))
}

for (cs in cases) {
  case_qmd <- file.path(dir_cases, paste0("case-", cs$n2, ".qmd"))
  soln_rel <- file.path("..", "solutions", paste0("solution-", cs$n2, ".qmd"))
  write_utf8(c(
    "---",
    paste0("title: \"", cs$n2, " \u00b7 ", cs$title, "\""),
    "---",
    "",
    paste0(GENERATED_NOTE),
    "::: {.case-meta}",
    badge_line(cs),
    "",
    tag_line(cs),
    ":::",
    "",
    cs$case_body,
    "",
    "------------------------------------------------------------------------",
    "",
    paste0("[**\u2192 Reveal the solution**](", soln_rel, "){.solution-link}"),
    ""
  ), case_qmd)

  soln_qmd <- file.path(dir_solns, paste0("solution-", cs$n2, ".qmd"))
  case_rel <- file.path("..", "cases", paste0("case-", cs$n2, ".qmd"))
  write_utf8(c(
    "---",
    paste0("title: \"", cs$n2, " \u00b7 ", cs$title, " \u2014 Solution\""),
    "---",
    "",
    paste0(GENERATED_NOTE),
    paste0("[\u2190 Back to the case](", case_rel, "){.back-link}"),
    "",
    tag_line(cs),
    "",
    "------------------------------------------------------------------------",
    "",
    cs$soln_body,
    ""
  ), soln_qmd)
}

