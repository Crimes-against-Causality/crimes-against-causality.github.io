# ---- intro pages from cases/*.md -------------------------------------

transform_simple_md <- function(src, dest, title, drop_from = NULL) {
  if (!file.exists(src)) {
    warning("Source not found, skipping: ", src); return(invisible())
  }
  lines <- readLines(src, warn = FALSE, encoding = "UTF-8")
  first_h1 <- which(str_detect(lines, "^#\\s"))[1]
  if (!is.na(first_h1)) lines <- lines[-(1:first_h1)]
  if (!is.null(drop_from)) {
    d <- heading_matching(lines, drop_from)
    if (length(d)) lines <- lines[seq_len(d[1] - 1)]
  }
  lines <- tidy_headings(lines)
  write_utf8(c("---", paste0("title: \"", title, "\""), "---", "",
               GENERATED_NOTE, lines), dest)
}
