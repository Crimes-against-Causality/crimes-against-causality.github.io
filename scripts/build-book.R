`%||%` <- function(a, b) if (is.null(a) || (length(a) == 1 && is.na(a))) b else a

# Write a character vector to disk as UTF-8, regardless of the session locale.
write_utf8 <- function(lines, path) {
  con <- file(path, open = "wb")
  on.exit(close(con))
  writeLines(enc2utf8(as.character(lines)), con, useBytes = TRUE)
}

# Strip the cosmetic inline HTML that some case files use in headings/text.
clean_inline_html <- function(x) {
  x <- str_replace_all(x, "<br\\s*/?>", " ")
  x <- str_replace_all(x, "</?span[^>]*>", "")
  str_squish(x)
}

# Read a case file and return list(meta = <yaml>, body = <character vector>).
read_case <- function(path) {
  lines <- readLines(path, warn = FALSE, encoding = "UTF-8")
  if (length(lines) == 0 || !str_detect(lines[1], "^---\\s*$")) {
    stop("Case file is missing YAML front matter: ", path)
  }
  close_idx <- which(str_detect(lines, "^---\\s*$"))[2]
  if (is.na(close_idx)) stop("Unterminated YAML front matter: ", path)
  meta <- yaml.load(paste(lines[2:(close_idx - 1)], collapse = "\n"))
  body <- lines[(close_idx + 1):length(lines)]
  # drop the leading H1 title line (we render the title from front matter)
  first_h1 <- which(str_detect(body, "^#\\s"))[1]
  if (!is.na(first_h1)) body <- body[-(1:first_h1)]
  # trim leading blank lines
  while (length(body) && !nzchar(str_trim(body[1]))) body <- body[-1]
  list(meta = meta, body = body)
}

# Index of heading lines whose (cleaned) text matches a regex, case-insensitive.
heading_matching <- function(body, pattern) {
  is_h    <- str_detect(body, "^#{1,6}\\s")
  cleaned <- clean_inline_html(str_replace(body, "^#{1,6}\\s*", ""))
  which(is_h & str_detect(cleaned, regex(pattern, ignore_case = TRUE)))
}

# Split a body into the case half and the solution half at the "Answers" heading.
split_case_solution <- function(body, slug) {
  ans <- heading_matching(body, "^answers?\\b")
  if (length(ans) == 0) {
    warning("No 'Answers' heading found in ", slug,
            " \u2014 treating whole file as the case.")
    return(list(case = body, solution = character(0)))
  }
  cut <- ans[1]
  list(case = body[seq_len(cut - 1)], solution = body[cut:length(body)])
}

# Copy referenced local images for one body part into <dest>/assets and rewrite
# the links. Returns the rewritten body.
relocate_images <- function(body, src_dir, dest_dir, prefix) {
  img_re <- "!\\[[^]]*\\]\\(([^)]+)\\)"
  for (i in seq_along(body)) {
    m <- str_match_all(body[i], img_re)[[1]]
    if (nrow(m) == 0) next
    for (j in seq_len(nrow(m))) {
      path <- m[j, 2]
      if (str_detect(path, "^https?://")) next
      src <- file.path(src_dir, path)
      if (!file.exists(src)) {
        warning("Missing image referenced in ", basename(src_dir), ": ", path)
        next
      }
      newname <- paste0(prefix, "-", basename(path))
      file.copy(src, file.path(dest_dir, "assets", newname), overwrite = TRUE)
      body[i] <- str_replace(body[i], stringr::fixed(path),
                             file.path("assets", newname))
    }
  }
  body
}

# Clean heading lines so the TOC / anchors are tidy (keeps body text as-is).
tidy_headings <- function(body) {
  is_h <- str_detect(body, "^#{1,6}\\s")
  body[is_h] <- vapply(body[is_h], function(h) {
    hashes <- str_extract(h, "^#{1,6}")
    paste(hashes, clean_inline_html(str_replace(h, "^#{1,6}\\s*", "")))
  }, character(1))
  body
}

# Fold "## Exhibit N: ..." headings into the crime-scene flow. Exhibits are part
# of the scene, not separate sections, so turn the heading into a bold label.
fold_exhibits <- function(body) {
  is_h <- str_detect(body, "^#{1,6}\\s")
  txt  <- str_replace(body, "^#{1,6}\\s*", "")
  hit  <- is_h & str_detect(txt, regex("^\\**exhibit\\b", ignore_case = TRUE))
  body[hit] <- paste0("**", str_trim(str_replace_all(txt[hit], "\\*+", "")), "**")
  body
}

difficulty_label <- function(d) {
  lbl <- BOOK$difficulty_labels[as.character(d)]
  if (is.na(lbl)) as.character(d) else unname(lbl)
}
difficulty_dots <- function(d) {
  d <- suppressWarnings(as.integer(d)); if (is.na(d)) return("")
  paste0(strrep("\u25cf", d), strrep("\u25cb", max(0, 3 - d)))
}

as_vec <- function(x) if (is.null(x)) character(0) else unlist(x, use.names = FALSE)

pad2 <- function(n) sprintf("%02d", as.integer(n))

# ---- discover cases ---------------------------------------------------------

case_dirs <- list.dirs(cases_src, recursive = FALSE, full.names = TRUE)
case_dirs <- case_dirs[str_detect(basename(case_dirs), "^[0-9]+-")]
case_dirs <- case_dirs[order(as.integer(str_extract(basename(case_dirs), "^[0-9]+")))]

cases <- list()
for (cd in case_dirs) {
  cf <- list.files(cd, pattern = "^case-file.*\\.md$", full.names = TRUE)
  if (length(cf) == 0) {
    warning("No case-file*.md in ", cd, " \u2014 skipping.")
    next
  }
  cf <- cf[1]
  parsed <- read_case(cf)
  meta   <- parsed$meta
  num    <- as.integer(meta$number %||% str_extract(basename(cd), "^[0-9]+"))
  n2     <- pad2(num)

  parts    <- split_case_solution(parsed$body, meta$slug %||% basename(cd))
  case_body <- fold_exhibits(tidy_headings(relocate_images(parts$case, cd, dir_cases, n2)))
  soln_body <- tidy_headings(relocate_images(parts$solution, cd, dir_solns, n2))

  cases[[length(cases) + 1]] <- list(
    number = num, n2 = n2, dir = cd,
    title = meta$title %||% basename(cd),
    detective = meta$detective %||% "",
    author = meta$author %||% "",
    difficulty = meta$difficulty %||% NA,
    topics = as_vec(meta$topics),
    fallacies = as_vec(meta$fallacies),
    summary = meta$summary %||% "",
    case_body = case_body, soln_body = soln_body
  )
}

