# ---- aggregated bibliography ------------------------------------------------

collect_sources <- function(cs) {
  b <- cs$soln_body
  h <- heading_matching(b, "^sources?\\b|^further reading\\b|^references?\\b")
  if (length(h) == 0) return(character(0))
  start <- h[1] + 1
  nxt <- which(str_detect(b, "^#{1,6}\\s"))
  nxt <- nxt[nxt > h[1]]
  end <- if (length(nxt)) nxt[1] - 1 else length(b)
  src <- b[start:end]
  src <- src[str_detect(src, "^\\s*-\\s")]
  str_trim(str_replace(src, "^\\s*-\\s*", ""))
}
all_src <- unique(unlist(lapply(cases, collect_sources)))
all_src <- all_src[nzchar(all_src)]
all_src <- all_src[order(tolower(all_src))]
write_utf8(c(
  "---", "title: \"References & Further Reading\"", "---", "",
  GENERATED_NOTE,
  "Sources cited across all cases, collected here for convenience. Each case",
  "also lists its own sources at the end of its solution.",
  "",
  if (length(all_src)) paste0("- ", all_src) else "_No sources found._"
), file.path(book_dir, "references.qmd"))

# ---- tag index --------------------------------------------------------------

build_index_block <- function(field, label) {
  tagmap <- list()
  for (cs in cases) {
    for (t in cs[[field]]) {
      tagmap[[t]] <- c(tagmap[[t]], cs$n2)
    }
  }
  if (length(tagmap) == 0) return(character(0))
  out <- paste0("## ", label, "\n")
  for (t in sort(names(tagmap))) {
    ns <- tagmap[[t]]
    links <- vapply(ns, function(n)
      paste0("[", n, "](cases/case-", n, ".qmd)"), character(1))
    out <- c(out, paste0("**", t, "** \u2014 ", paste(links, collapse = ", "), "\n"))
  }
  c(out, "")
}
idx <- c(
  "---", "title: \"Index\"", "---", "",
  GENERATED_NOTE,
  "Find cases by what they are about and by the kind of reasoning error they",
  "expose.",
  "",
  build_index_block("topics", "By topic"),
  build_index_block("fallacies", "By fallacy"),
  build_index_block_difficulty <- {
    tagmap <- list()
    for (cs in cases) {
      lbl <- difficulty_label(cs$difficulty)
      tagmap[[lbl]] <- c(tagmap[[lbl]], cs$n2)
    }
    out <- "## By difficulty\n"
    for (lbl in c("Easy", "Medium", "Hard")) {
      if (is.null(tagmap[[lbl]])) next
      links <- vapply(tagmap[[lbl]], function(n)
        paste0("[", n, "](cases/case-", n, ".qmd)"), character(1))
      out <- c(out, paste0("**", lbl, "** \u2014 ", paste(links, collapse = ", "), "\n"))
    }
    out
  }
)
write_utf8(idx, file.path(book_dir, "appendix-index.qmd"))

# ---- intro pages from case-studies/*.md -------------------------------------

transform_simple_md <- function(src, dest, title, drop_from = NULL) {
  if (!file.exists(src)) {
    warning("Source not found, skipping: ", src); return(invisible())
  }
  lines <- readLines(src, warn = FALSE, encoding = "UTF-8")
  first_h1 <- which(str_detect(lines, "^#\\s"))[1]
  if (!is.na(first_h1)) lines <- lines[-(1:first_h1)]
  if (!is.null(drop_from)) {
    d <- heading_matching(lines, drop_from)
    if (length(d)) lines <- lines[seq_len(d[1] - 1)]
  }
  lines <- tidy_headings(lines)
  write_utf8(c("---", paste0("title: \"", title, "\""), "---", "",
               GENERATED_NOTE, lines), dest)
}
transform_simple_md(file.path(cases_src, "detectives.md"),
                    file.path(dir_intro, "detectives.qmd"),
                    "The Detectives", drop_from = "^case assignments")
transform_simple_md(file.path(cases_src, "lore.md"),
                    file.path(dir_intro, "world.qmd"),
                    "The World of Harland")

