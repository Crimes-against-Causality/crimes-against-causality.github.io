# ---- aggregated bibliography ------------------------------------------------

collect_sources <- function(cs) {
  b <- cs$soln_body
  h <- heading_matching(b, "^sources?\\b|^further reading\\b|^references?\\b")
  if (length(h) == 0) return(character(0))
  start <- h[1] + 1
  nxt <- which(str_detect(b, "^#{1,6}\\s"))
  nxt <- nxt[nxt > h[1]]
  end <- if (length(nxt)) nxt[1] - 1 else length(b)
  src <- b[start:end]
  src <- src[str_detect(src, "^\\s*-\\s")]
  str_trim(str_replace(src, "^\\s*-\\s*", ""))
}
all_src <- unique(unlist(lapply(cases, collect_sources)))
