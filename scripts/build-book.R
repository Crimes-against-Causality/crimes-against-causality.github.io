# ---- tag index --------------------------------------------------------------

build_index_block <- function(field, label) {
  tagmap <- list()
  for (cs in cases) {
    for (t in cs[[field]]) {
      tagmap[[t]] <- c(tagmap[[t]], cs$n2)
    }
  }
  if (length(tagmap) == 0) return(character(0))
  out <- paste0("## ", label, "\n")
  for (t in sort(names(tagmap))) {
    ns <- tagmap[[t]]
    links <- vapply(ns, function(n)
      paste0("[", n, "](cases/case-", n, ".qmd)"), character(1))
    out <- c(out, paste0("**", t, "** \u2014 ", paste(links, collapse = ", "), "\n"))
  }
  c(out, "")
}
